using System;

namespace FloatingPointValues
{
    class FloatingPointValues
    {
        static void Main()
        {
            float numberOne = 1.234f;
            float numberTwo = 1.2e-3f;
            float numberThree = (float)1 / 3;

            Console.WriteLine(numberOne);
            Console.WriteLine(numberTwo);
            Console.WriteLine(numberThree);
        }
    }
}
